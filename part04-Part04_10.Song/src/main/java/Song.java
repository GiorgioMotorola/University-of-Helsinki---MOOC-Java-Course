
public class Song {
    private String name;
    private int length;
    
    public Song(String name, int length) {
         this.name = name;
         this.length = length;
        
    } //construct
    
    public String name() {
        return name;
        
    } //method 1
    
    public int length() {
        return length;
        
    } //method 2
    
} //end
