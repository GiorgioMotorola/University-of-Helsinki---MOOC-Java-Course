
public class Film {
    private String name;
    private int ageRating;
    
    public Film(String filmName, int filmAgeRating) {
        this.name = filmName;
        this.ageRating = filmAgeRating;
    } //end of con
    
    public String name() {
        return name;
        
    } //method one
    
    public int ageRating() {
        return ageRating;
        
    } //method 2
} // end
